public class Augment{
    // This is arugmented Example
    public static void main(String[] args){
        int a=10;
        int b=5;
        a+=b;
        System.out.println(a);

        int c=10;
        int d=5;
        c-=d;
        System.out.println(c);

        int e=10;
        int f=5;
        e*=f;
        System.out.println(e);

        int g=10;
        int h=5;
        g/=h;
        System.out.println(g);

        int i=10;
        int j=5;
        i%=j;
        System.out.println(i);
    }
}